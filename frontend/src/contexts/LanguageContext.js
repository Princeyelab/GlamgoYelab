const LanguageContext = createContext();

export function LanguageProvider({ children }) {
  const [language, setLanguage] = useState('fr');
  const [isRTL, setIsRTL] = useState(false);
  const [isLoaded, setIsLoaded] = useState(false);

  // Charger la langue sauvegardée
  useEffect(() => {
    const savedLanguage = localStorage.getItem('glamgo_language');
    if (savedLanguage && (savedLanguage === 'fr' || savedLanguage === 'ar')) {
      setLanguage(savedLanguage);
      setIsRTL(savedLanguage === 'ar');
    }
    setIsLoaded(true);
  }, []);

  // Appliquer la direction RTL
  useEffect(() => {
    document.documentElement.dir = isRTL ? 'rtl' : 'ltr';
    document.documentElement.lang = language;

    // Ajouter/retirer la classe RTL pour le styling
    if (isRTL) {
      document.body.classList.add('rtl');
    } else {
      document.body.classList.remove('rtl');
    }
  }, [isRTL, language]);

  // Changer de langue
  const toggleLanguage = useCallback(() => {
    const newLang = language === 'fr' ? 'ar' : 'fr';
    setLanguage(newLang);
    setIsRTL(newLang === 'ar');
    localStorage.setItem('glamgo_language', newLang);
  }, [language]);

  // Fonction de traduction (clés statiques)
  const t = useCallback((key, params = {}) => {
    let text = translations[language][key] || translations['fr'][key] || key;

    // Remplacer les paramètres {param}
    Object.keys(params).forEach(param => {
      text = text.replace(`{${param}}`, params[param]);
    });

    return text;
  }, [language]);

  // Traduction dynamique avec DeepL (pour contenu de la BDD)
  const translateDynamic = useCallback(async (text) => {
    if (!text || language === 'fr') {
      return text; // Pas besoin de traduire si c'est déjà en français
    }
    // Traduire vers l'arabe
    return await translateText(text, 'AR', 'FR');
  }, [language]);

  // Traduction batch dynamique (plus efficace pour plusieurs textes)
  const translateDynamicBatch = useCallback(async (texts) => {
    if (!texts || texts.length === 0 || language === 'fr') {
      return texts;
    }
    return await translateBatch(texts, 'AR');
  }, [language]);

  // Traduire un objet (catégorie, service, etc.)
  const translateDynamicObject = useCallback(async (obj, keys) => {
    if (!obj || language === 'fr') {
      return obj;
    }
    return await translateObject(obj, keys, 'AR');
  }, [language]);

  const contextValue = useMemo(() => ({
    language,
    setLanguage,
    toggleLanguage,
    t,
    isRTL,
    isLoaded,
    translations: translations[language],
    // Nouvelles fonctions de traduction dynamique DeepL
    translateDynamic,
    translateDynamicBatch,
    translateDynamicObject
  }), [language, toggleLanguage, t, isRTL, isLoaded, translateDynamic, translateDynamicBatch, translateDynamicObject]);

  // Ne pas rendre les enfants tant que la langue n'est pas chargée depuis localStorage
  // Cela évite le flash de contenu en français quand l'utilisateur a choisi l'arabe
  if (!isLoaded) {
    return null;
  }

  return (
    <LanguageContext.Provider value={contextValue}>
      {children}
    </LanguageContext.Provider>
  );
}

export function useLanguage() {
  const context = useContext(LanguageContext);
  if (!context) {
    throw new Error('useLanguage must be used within a LanguageProvider');
  }
  return context;
}

export default LanguageContext;
